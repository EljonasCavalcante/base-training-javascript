var cor1 = 'Verde' ; // cores para o usuário escolher Azul, Amarelo, Verde  ou nenhuma.

switch (cor1) {
    case 'Azul':
        console.log('Olhe para o céu')
        break;
    case 'Amarelo':
        console.log('Olhe para o sol')
        break;
    case 'Verde':
        console.log('Olhe para a floresta')
        break;
    default:
        console.log('Feche os olhos')
}